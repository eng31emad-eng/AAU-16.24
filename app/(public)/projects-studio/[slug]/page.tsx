import { notFound } from 'next/navigation';
import { projectsService } from '@/services/data/projects.service.mock';
import ProjectDetailsContent from '@/components/ProjectDetailsContent';
import { Metadata } from 'next';

export const revalidate = 86400; // 1 day

interface PageProps {
    params: Promise<{ slug: string }>;
}

export async function generateStaticParams() {
    const projects = await projectsService.getAll();
    return projects.map((project) => ({
        slug: project.slug,
    }));
}

export async function generateMetadata({ params }: PageProps): Promise<Metadata> {
    const { slug } = await params;
    const project = await projectsService.getBySlug(slug);

    if (!project) {
        return {
            title: 'Project Not Found',
        };
    }

    return {
        title: `${project.titleEn} | Projects`,
        description: project.descEn,
        openGraph: {
            title: `${project.titleEn} | Projects`,
            description: project.descEn,
        },
    };
}

export default async function ProjectDetailsPage({ params }: PageProps) {
    const { slug } = await params;
    const project = await projectsService.getBySlug(slug);

    if (!project) {
        return notFound();
    }

    const allProjects = await projectsService.getAll();
    const relatedProjects = allProjects
        .filter(p => p.id !== project.id && p.status === project.status)
        .slice(0, 3);

    return <ProjectDetailsContent project={project} relatedProjects={relatedProjects} />;
}
